let coordinates = (x: 3, y: 3, z: 5)

switch coordinates {
case (0, 0, 0): // 1
  "Origin"
case (_, 0, 0): // 2
  "On the x-axis."
case (0, _, 0): // 3
  "On the y-axis."
case (0, 0, _): // 4
  "On the z-axis."
default:        // 5
  "Somewhere in space"
}

//================================================================
switch coordinates {
case (0, 0, 0):
  "Origin"
case (let x, 0, 0):
  "On the x-axis at x = \(x)"
case (0, let y, 0):
  "On the y-axis at y = \(y)"
case (0, 0, let z):
  "On the z-axis at z = \(z)"
case let (x, y, z):
  "Somewhere in space at x = \(x), y = \(y), z = \(z)"
}

//================================================================
